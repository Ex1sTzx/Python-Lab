def slice_operations():
    string = "Hello, World!"
    print("Original string:", string)
    
    print("First 5 characters:", string[:5])
    
    print("Last 5 characters:", string[-5:])
    
    print("Characters from index 2 to 7:", string[2:7])
    
    print("Characters from index 2 to the end:", string[2:])
    
    print("Characters from the beginning to index 7:", string[:7])
    
    print("Reversed string:", string[::-1])

slice_operations()