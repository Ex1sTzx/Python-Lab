# Demonstrate basic data types in Python

# Integer
num = 10
print("Type of num:", type(num))

# Float
float_num = 3.14
print("Type of float_num:", type(float_num))

# String
name = "Python"
print("Type of name:", type(name))

# Boolean
is_true = True
print("Type of is_true:", type(is_true))

# List
list_data = [1, 2, 3, 4]
print("Type of list_data:", type(list_data))

# Tuple
tuple_data = (1, 2, 3, 4)
print("Type of tuple_data:", type(tuple_data))

# Dictionary
dict_data = {"name": "John", "age": 30}
print("Type of dict_data:", type(dict_data))